namespace Glippy.Core
{
	/// <summary>
	/// Unicode characters used in item labels.
	/// </summary>
	public static class UnicodeCharacters
	{
		/// <summary>
		/// Image symbol (U+25A3).
		/// </summary>
		public const string Image = "\u25A3";
		
		/// <summary>
		/// Return symbol (U+21B2).
		/// </summary>
		public const string Return = "\u21B2";
		
		/// <summary>
		/// Tab symbol (U+21C4).
		/// </summary>
		public const string Tab = "\u21C4";
		
		/// <summary>
		/// Keyboard symbol (U+2328).
		/// </summary>
		public const string Keyboard = "\u2328";

		/// <summary>
		/// Text cursor symbol (U+2336).
		/// </summary>
		public const string Cursor = "\u2336";
		
		/// <summary>
		/// Scissors symbol (U+2702).
		/// </summary>
		public const string Scissors = "\u2702";
		
		/// <summary>
		/// Closet symbol (U+2339).
		/// </summary>
		public const string Closet = "\u2339";
		
		/// <summary>
		/// Tag symbol (U+226F).
		/// </summary>
		public const string Tag = "\u226F";
	}
}
